package day03_escape_sequence;

public class EscapeSequence {
    public static void main(String[] args){
        System.out.println("I like \"java\"programing");
        System.out.println("Let's buy \"coffee\" and \"tea\"");
        System.out.println("\\");
        System.out.println("java\nintellij");
        System.out.println("\tjava\tselenium\tapi");
    }
}
