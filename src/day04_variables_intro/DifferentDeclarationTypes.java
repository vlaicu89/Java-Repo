package day04_variables_intro;

public class DifferentDeclarationTypes {
    public static void main(String[] args) {
        //declaration + assignement in single statement:
        int bananas = 34;
        System.out.println(bananas);

        int students = 400;
        System.out.println(students);
        students = 401;
        System.out.println(students);

        int peopleInCity, birdsOnTree, fishOnLake;
        peopleInCity = 1000;
        birdsOnTree = 56;
        fishOnLake = 765;

        System.out.println(peopleInCity);
        System.out.println(birdsOnTree);
        System.out.println(fishOnLake);

        int studentsInIL = 56, studentsInVA = 58;
        System.out.println(studentsInIL);
         System.out.println(studentsInVA);
    }


}
